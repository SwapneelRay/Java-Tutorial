public class Ops {
public static void main(String[] args) {
    //operators +,-,/,*,=,%
    //unary ops
    int x = 10;
    System.out.println(x--);// postfix
    System.out.println(x);
    int y = 10;
    System.out.println(--y);// prefix
    System.out.println(y);

    //arithmetic ops
    int a = 3132;
    int b = 3;
    System.out.println(a%b);//modulus ops
    
}
}
