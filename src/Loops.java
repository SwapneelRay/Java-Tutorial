public class Loops {
    public static void main(String[] args) {
        //for loop
        //for(initialization;condtion;increment/decrement){statement;}
        int a;
        for(a=0;a<=50;a++){
            System.out.println(a);
        }
        
        //for each

        // while loop
        int i=0;
        while (i<=10) {
            System.out.println(i);
            i=i+2;
        }

        // do-while loop
        int j=0;
        do{
            System.out.println(j);
            j++;
        }while(j<=10);

        
    }
}
